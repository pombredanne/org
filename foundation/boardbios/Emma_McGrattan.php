Emma McGrattan is senior vice president of engineering at Ingres. She 
is responsible for technology integration and the development of emerging 
technologies. A leading authority in DBMS technologies, Emma has been 
instrumental in the ongoing success of the Ingres product line. Emma 
joined Ingres from CA where she held a similar position and was 
responsible for the Ingres family of relational database management 
products. She started her career with Ingres in 1992, and has held a 
variety of senior development and management positions.  Born in Ireland, 
Emma earned a Bachelor of Electronic Engineering from Dublin City University.