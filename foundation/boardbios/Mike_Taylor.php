<p>Mike Taylor is President, CEO, and co-founder of Instantiations, Inc. and has 
been involved with Eclipse since late 1999. He has extensive senior management 
experience in the enterprise software development tools arena. During 
his 30-year career in the software industry Mike has been involved in 
founding several successful high-tech software companies.</p>
 
<p>Mike holds a BA and and MBA from the University of Washington. In addition 
to his software-related business activities he is a private pilot, 
past-president of the Oregon Archaeological Society, and has co-authored 
several books on Pacific Northwest Indian rock art. Mike lives happily 
with his wife and dog in Vancouver, Washington.</p>