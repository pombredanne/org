<p>Eric Clayberg is a Software Engineering Manager at Google and former
co-founder and VP of Product Development for Instantiations (acquired
by Google in 2010). He currently leads the Google Web Toolkit (GWT)
and Google Plugin for Eclipse (GPE) projects and is the Project Lead
for the WindowBuilder project at Eclipse.org
(http://www.eclipse.org/windowbuilder).</p> 

<p>He holds a BS degree in Aerospace Engineering from MIT, an MBA from
Harvard, and is co-author of "Eclipse Plug-ins" (3rd Edition, Addison
Wesley , Dec 2008), and "The Eclipse Graphical Editing Framework
(GEF)" (Addison Wesley, Aug 2011). See http://www.qualityeclipse.com.</p>

<p>In his spare time, he enjoys one of world's largest collections of classic coin-op video games.</p>
