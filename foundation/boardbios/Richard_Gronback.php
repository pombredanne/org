Richard Gronback is chief scientist for modeling products at Borland 
Software Corporation, managing both open source and commercial product 
development.  Richard co-leads the Modeling project Project Management 
Committee (PMC), along with Ed Merks of IBM, and leads the Graphical 
Modeling Framework (GMF) project.  Richard holds a BSE in Computer 
Science & Engineering from the University of Connecticut, and was a 
reactor operator in the U.S. Navy prior to his current career in software.
