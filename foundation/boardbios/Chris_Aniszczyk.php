Chris Aniszczyk is the technical lead for the Eclipse Plug-in
Development Environment (PDE) project and a Principal Software
Engineer at Red Hat. He tends to be all over the place inside the
Eclipse community by committing to and mentoring various Eclipse
projects. He sits on the Eclipse Architecture Council and on the
Eclipse Technology PMC. His passions are blogging, software advocacy,
tooling and anything Eclipse. He's always available to discuss open
source or Eclipse over a frosty beverage.
