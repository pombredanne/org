Dave Thomson is a Distinguished Engineer at IBM's Ottawa lab (Canada). Dave 
was one of the founding employees of Object Technology International (OTI), 
a company IBM acquired in 1996. Prior to OTI's integration with IBM Dave 
served as VP Development at OTI. Dave led the teams that delivered the 
underlying IDE technology in many of IBM's award-winning products including: 
VisualAge for Smalltalk, VisualAge for Java, and the Eclipse platform and was 
a key contributor to the ANSI Smalltalk specification. Dave led the transition 
of IBM's internal IDE platform development effort into the open source project 
we now know as Eclipse, and is one of the visionaries behind the Jazz Project. 
Dave is responsible for IBM's Eclipse and Jazz Development teams, and serves 
as a Director on the board of the Eclipse Foundation. 