Tracy Ragan has served as CEO and COO for OpenMake Software since 1995. She began 
her professional IT career in 1989 consulting to Fortune 500 organizations in 
the areas of testing, configuration management and build management. It was 
during her consulting experiences that Tracy recognized the lack of 
build management procedures for the distributed platform that had long 
been considered standard on the mainframe. In the four years leading to 
the creation of OpenMake Software she worked with development teams in 
implementing a team-centric standardized build management process. Tracy received 
her Bachelor of Science Degree in Business Administration 
from California Polytechnic University, Pomona, CA.